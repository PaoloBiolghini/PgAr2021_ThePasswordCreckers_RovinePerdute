package it.unibs.fp.Prova;


import java.lang.Math;

public class Posizione {
	
	private double x;
	private double y;
	private double z;
	
    public Posizione(double _x,double _y, double _z)
    {
    	this.x=_x;
    	this.y=_y;
    	this.z=_z;	
    }

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

	public double getZ() {
		return z;
	}
}
